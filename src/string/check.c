#include "minishell.h"

int	is_whitespace_eos(char c)
{
	if (c == '\0' || c == ' ' || c == '\f' || c == '\n' || c == '\r'
		|| c == '\t' || c == '\v')
		return (TRUE);
	else
		return (FALSE);
}
